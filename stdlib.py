def EQ(A,B):
    if (A == B):
        return True
    else:
        return False

def NEQ(A,B):
    if (A != B):
        return True
    else:
        return False

def LT(A,B):
    if (A < B):
        return True
    else:
        return False

def GT(A,B):
    if (A > B):
        return True
    else:
        return False

def LEQ(A,B):
    if (A <= B):
        return True
    else:
        return False

def GEQ(A,B):
    if (A >= B):
        return True
    else:
        return False

def ADD(A,B):
    return (A+B)

def SUB(A,B):
    return (A-B)

